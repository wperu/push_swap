#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	result;

	j = 0;
	i = ft_strlen(dst);
	result = ft_strlen(src);
	if (size <= i)
		return (result = result + size);
	else
		result = result + i;
	while (src[j] != '\0' && i + 1 < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (result);
}
